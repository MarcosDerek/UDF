floatNum1 = float(input("Digite o primeiro numero inteiro: "))
floatNum2 = float(input("Digite o segundo numero inteiro: "))

print("Os numeros {} e {} somados correspondem a {}".format(floatNum1, floatNum2, floatNum1 + floatNum2))
print("Os numeros {} e {} subtraidos correspondem a {}".format(floatNum1, floatNum2, floatNum1 - floatNum2))
print("O produto dos numeros {} e {} correspondem a {}".format(floatNum1, floatNum2, floatNum1 * floatNum2))
print("A divisão dos numeros {} e {} correspondem a {}".format(floatNum1, floatNum2, floatNum1 / floatNum2))